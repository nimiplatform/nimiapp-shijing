export function buildShiJingAnswerBrief(mode: ShiJingAnswerBriefMode): string {
  const outputBoundary =
    mode === 'json_answer_field'
      ? 'Runtime 输出仍然必须是 JSON wording patch；请把下列结构完整写入 answer 字段，不要把这些标题作为 JSON 顶层字段。'
      : '请直接输出下列结构化正文，不要额外包裹 JSON、Markdown 代码块或内部字段名。';
  return [
    'ShiJing 问镜回答 writing requirements:',
    outputBoundary,
    ANSWER_REQUIREMENTS,
    ANSWER_STRUCTURE,
  ].join('\n\n');
}
